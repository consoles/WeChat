package org.gpf.trans;

public class Parts {

	private String parts;
	private String[] means;
	public String getParts() {
		return parts;
	}
	public void setParts(String parts) {
		this.parts = parts;
	}
	public String[] getMeans() {
		return means;
	}
	public void setMeans(String[] means) {
		this.means = means;
	}
}
